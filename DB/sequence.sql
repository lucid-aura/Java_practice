/*
    SEQUENCE : 유일한 값을 생성해 주는 오라클 오브젝트
                NUMBER++;
*/

-- SEQUENCE 생성
CREATE SEQUENCE TEST_SEQ
INCREMENT BY 1  -- 1씩 증가
START WITH 10   -- 시작 숫자
MAXVALUE 100
MINVALUE 1;

-- CURRVAL - 현재의 SEQ
SELECT TEST_SEQ.CURRVAL
FROM DUAL;
    
-- NEXTVAL = 진행 값 먼저 한번 진행해줘야함
SELECT TEST_SEQ.NEXTVAL
FROM DUAL;

-- 수정
ALTER SEQUENCE TEST_SEQ
INCREMENT BY 3;

DROP SEQUENCE TEST_SEQ; -- 시퀀스는 처음부터 시작이 안됨(지웠다가 다시 해야함)

DROP TABLE STUDENT;

CREATE TABLE STUDENT(
    NO INTEGER,
    NAME VARCHAR2(30),
    HEIGHT NUMBER(5, 1)
);

INSERT INTO STUDENT(NO, NAME, HEIGHT)
VALUES(TEST_SEQ.NEXTVAL, '홍길동', 161.1);

SELECT * FROM STUDENT;

